    <div>220400 «Управление в технических системах»</div>
</h1>
<div class="classic_text">
    <p>
        Проектно-конструкторская деятельность. Анализ состояния научно-технической проблемы путем подбора, изучения и
        анализа литературных и патентных источников в области автоматизации и управления; определение цели, постановка
        задач проектирования, подготовка технических заданий на выполнение проектных работ; проектирование средств и
        систем автоматизации и управления с использованием современных пакетов прикладного программного обеспечения
        автоматизированного проектирования; разработка проектно-конструкторской документации в соответствии с
        нормативными требованиями.
    </p>


