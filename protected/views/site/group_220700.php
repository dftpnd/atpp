    <p>
        Выпускник по направлению подготовки «Автоматизация технологических процессов и производств» 220700 может
        выполнять следующие виды профессиональной деятельности.
    </p>

    <p>
        Проектно-конструкторская деятельность. Формулирование целей проекта, построение структуры их взаимосвязей,
        разработка проектов автоматизации процессов и производств; разработка функциональной, логической и технической
        организации автоматизации, автоматизированных систем, их технического, алгоритмического и программного
        обеспечения на основе современных средств и технологий проектирования; разработка технической документации для
        систем автоматизации и управления.
    </p>
    <img src='/i/foto11234234.jpg' width="100%" class="classic_picter">

    <div class="picter_hint">
        Занятия в центре обучения «КГЭУ, Сименс и КЭР-Инжиниринг»
    </div>
    <p>
        Производственно-технологическая деятельность. Освоение на практике и совершенствование систем и средств
        автоматизации и управления производственными и технологическими процессами изготовления продукции, ее жизненным
        циклом и качеством; обеспечение мероприятий по улучшению качества продукции, совершенствованию технологического,
        метрологического, материального обеспечения ее изготовления.
    </p>

    <p>
        <strong>Организационно-управленческая деятельность</strong>. Организация работы малых коллективов исполнителей,
                планирование
                работы
                персонала и фондов оплаты труда, принятие управленческих решений на основе экономических расчетов;
                участие в
                разработке мероприятий по организации процессов разработки, изготовления, контроля, испытаний и
                внедрения
                продукции
                средств и систем автоматизации, контроля, диагностики, управления производством, жизненным циклом
                продукции и ее
                качеством, их эффективной эксплуатации.
    </p>

    <p>
        <strong>Научно-исследовательская деятельность.</strong> Изучение научно-технической информации, опыта в области
        автоматизации,
        автоматизированного управления жизненным циклом продукции, компьютерных систем управления ее качеством;
        моделирование продукции, технологических процессов, средств и систем автоматизации, контроля,
        диагностики,
        испытаний и управления с использованием стандартных пакетов и средств автоматизированного
        проектирования;
        участие в разработке алгоритмического и программного обеспечения; проведение экспериментов.
    </p>

    <p>
        <strong> Сервисно-эксплуатационная деятельность.</strong> Настройка и регламент-ное эксплуатационное
        обслуживание технических и
        программных средств систем автоматизации; выбор методов и средств измерения эксплуатационных характеристик
        средств
        автоматизации; инсталляция, настройка и обслуживание системного, инструментального и прикладного программного
        обеспечения систем автоматизации и управления, диагностика технологических процессов.
    </p>

    <p>
        <strong>Специальные виды деятельности.</strong> Организация повышения квалифи-кации и тренинга сотрудников
        подразделений в
        области
        автоматизации технологических процессов и производств, автоматизированного управления жизненным циклом продукции
        и
        ее качеством.
    </p>

