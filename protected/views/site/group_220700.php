<h1>
    <div>Бакалаврские образовательные программы кафедры АТПП</div>
    <div>Направление 220700 «Автоматизация технологических процессов и производств (в энергетике)</div>
</h1>
<div class="classic_text">


    <p>
        Выпускник бакалавриата по направлению подготовки 220700 «Автоматизация технологических процессов и производств
        (в энергетике)» может занимать непосредственно после окончания вуза следующие должности: инженер;
        инженер-технолог; инженер по автоматизации и механизации производственных процессов; инженер по
        автоматизированным системам управления производством; инженер-программист (программист); инженер по наладке и
        испытаниям и прочие.
    </p>

    <p>
        Области профессиональной деятельности. Совокупность средств и ме-тодов человеческой деятельности в энергетике,
        направленных на автоматиза-цию действующих и создание новых автоматизированных производств, средств
        автоматизации, применение алгоритмического, аппаратного и программного обеспечения, обеспечивающих выпуск
        конкурентоспособной продукции, ее жизненным циклом продукции и ее качеством, совершенствование структур и
        процессов промышленных предприятий в рамках единого информационного пространства.
    </p>

    <p>
        Объектами профессиональной деятельности выпускника являются производственные, автоматические и
        автоматизированные системы, диагно-стирования производств, их математическое, программное, информационное и
        техническое обеспечение, а также методы и средства их проектирования, изготовления, отладки, производственных
        испытаний и научного исследо-вания.
    </p>

